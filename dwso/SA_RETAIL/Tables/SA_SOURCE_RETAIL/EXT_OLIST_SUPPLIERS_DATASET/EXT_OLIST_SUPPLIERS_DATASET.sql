CREATE TABLE EXT_OLIST_SUPPLIERS_DATASET (
  SUPPLIER_ID     VARCHAR2(250),
  SUPPLIER_CITY   VARCHAR2(250),
  SUPPLIER_STATE  VARCHAR2(250),
  SUPPLIER_NAME   VARCHAR2(250),
  SECTOR          VARCHAR2(250)
)
ORGANIZATION EXTERNAL (
  TYPE ORACLE_LOADER
  DEFAULT DIRECTORY EXT_DIR_RETAIL_DATA
  ACCESS PARAMETERS (
    RECORDS DELIMITED BY NEWLINE 
    SKIP 1
    FIELDS TERMINATED BY ','
    OPTIONALLY ENCLOSED BY '"'
    MISSING FIELD VALUES ARE NULL
  )
  LOCATION ('olist_suppliers_dataset.csv')
)
REJECT LIMIT UNLIMITED;

CREATE OR REPLACE PUBLIC SYNONYM EXT_OLIST_SUPPLIERS_DATASET FOR SA_SOURCE_SYSTEM_RETAIL.EXT_OLIST_SUPPLIERS_DATASET;
GRANT SELECT ON EXT_OLIST_SUPPLIERS_DATASET TO BL_CL;
